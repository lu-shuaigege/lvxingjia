第二条旅游行程单 旅行社应当提供带团号的《旅游行程单》（以下简称《行程单》），经双方签字或者盖章确认 后作为本合同的组成部分。《行程单》应当对如下内容作出明确的说明：
    （1）旅游行程的出发地、途经地、目的地、结束地，线路行程时间和具体安排（按自然日计含乘飞机、车、船等在途时间，不足 24 小时以一日计）； 
    （2）地接社的名称、地址、联系人和联系电话； 
    （3）交通服务安排及其标准（明确交通工具及档次等级、出发时间以及是否需中转等信息）
    （4）住宿服务安排及其标准（明确住宿饭店的名称、地点、星级，非星级饭店应当注明是否空调、热水、独立卫生间等相关服务设施）； 
    （5）用餐（早餐和正餐）服务安排及其标准（明确用餐次数、地点、标准）； 
    （6）旅行社统一安排的游览项目的具体内容及时间（明确旅游线路内容包括景区点及游览项名称等，景区点停留的最少时间）； 
    （7）自由活动的时间； 
    （8）行程安排的娱乐活动（明确娱乐活动的时间、地点和项目内容）； 《行程单》用语须准确清晰，在表明服务标准用语中不应当出现“准×星级”、“豪华”、 “仅供参考”、“以××为准”、“与××同级”等不确定用语。 

