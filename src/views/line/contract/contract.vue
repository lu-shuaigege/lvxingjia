第九条 合同的变更 
    1、旅行社与旅游者双方协商一致，可以变更本合同约定的内容，但应当以书面形式由双方签字 
    确认。由此增加的旅游费用及给对方造成的损失，由变更提出方承担；由此减少的旅游费用，旅行社应当退还旅游者。 
    2、行程开始前遇到不可抗力或者旅行社、履行辅助人已尽合理注意义务仍不能避免的事件的， 双方经协商可以取消行程或者延期出行。取消行程的，按照本合同第十四条处理；延期出行的，增 加的费用由旅游者承担，减少的费用退还旅游者。 
    3、行程中遇到不可抗力或者旅行社、履行辅助人已尽合理注意义务仍不能避免的事件，影响旅 游行程的，按以下方式处理： 
    （1）合同不能完全履行的，旅行社经向旅游者作出说明，旅游者同意变更的，可以在合理范围 内变更合同，因此增加的费用由旅游者承担，减少的费用退还旅游者。 
    （2）危及旅游者人身、财产安全的，旅行社应当采取相应的安全措施，因此支出的费用，由旅 行社与旅游者分担。 
    （3）造成旅游者滞留的，旅行社应采取相应的安置措施。因此增加的食宿费用由旅游者承担， 增加的返程费用双方分担。 

第十条 合同的转让 
    旅游行程开始前，旅游者可以将本合同中自身的权利义务转让给第三人，旅行社没有正当理由 的不得拒绝，并办理相关转让手续，因此增加的费用由旅游者和第三人承担。 正当理由包括但不限于：对应原报名者办理的相关服务不可转让给第三人的；无法为第三人安排交通等情形的；旅游活动对于旅游者的身份、资格等有特殊要求的。 

第十一条 不成团的安排 
    当旅行社组团未达到约定的成团人数不能成团时，旅游者可以与旅行社就如下安排在本合同第 

第二十三条中做出约定。 
    1、转团：旅行社可以在保证所承诺的服务内容和标准不降低的前提下，经事先征得旅游者书面 同意，委托其他旅行社履行合同，并就受委托出团的旅行社违反本合同约定的行为先行承担责任， 再行追偿。旅游者和受委托出团的旅行社另行签订合同的，本合同的权利义务终止。
    2、延期出团和改变线路出团：旅行社经征得旅游者书面同意，可以延期出团或者改变其他线路出团，因此增加的费用由旅游者承担，减少的费用旅行社予以退还。需要时可以重新签订旅游合同。
<div class="center b">第五章 合同的解除</div>
第十二条 旅行社解除合同 
    1、未达到约定的成团人数不能成团时，旅行社解除合同的，应当采取书面等有效形式。旅行社在行程开始前 7 日（按照出发日减去解除合同通知到达日的自然日之差计算，下同）以上（含第 7 日，下同）提出解除合同的，不承担违约责任，旅行社向旅游者退还已收取的全部旅游费用；旅行 社在行程开始前 7 日以内（不含第 7 日，下同）提出解除合同的，除向旅游者退还已收取的全部旅游费用外，还应当按本合同第十七条第 1 款的约定，承担相应的违约责任。 
    2、旅游者有下列情形之一的，旅行社可以解除合同： 
    （1）患有传染病等疾病，可能危害其他旅游者健康和安全的； 
    （2） 携带危害公共安全的物品且不同意交有关部门处理的； 
    （3）从事违法或者违反社会公德的活动的； 
    （4）从事严重影响其他旅游者权益的活动，且不听劝阻、不能制止的； 
    （5）法律、法规规定的其他情形。 
    旅行社因上述情形解除合同的，应当以书面等形式通知旅游者，按照本合同第十五条相关约定扣除必要的费用后，将余款退还旅游者。 

第十三条 旅游者解除合同 
